export function parseOrderCharSpec(order: any): {
  type: "builder" | "custom" | "leave_it" | "unknown";
  gender?: string; rel?: string; pers?: string;
  refChar?: string; customDesc?: string;
} {
  const name: string = order.character_name ?? "";
  const notes: string = order.notes ?? "";

  if (name === "おまかせ" || notes.includes("【おまかせ】")) {
    return { type: "leave_it" };
  }

  if (name.startsWith("[ビルダー]") || notes.includes("【キャラビルダー】")) {
    const genderM = notes.match(/性別[：:]\s*(.+)/);
    const relM    = notes.match(/関係性[：:]\s*(.+)/);
    const persM   = notes.match(/性格[：:]\s*(.+)/);
    return {
      type: "builder",
      gender: genderM?.[1]?.trim(),
      rel:    relM?.[1]?.trim(),
      pers:   persM?.[1]?.trim(),
    };
  }

  if (name === "オリジナル定義" || notes.includes("【オリジナル定義】")) {
    const refM  = notes.match(/参考[：:]\s*(.+)/);
    // 「キャラクター説明:」以降のテキストを抽出
    const descM = notes.match(/キャラクター説明[：:]?\s*\n([\s\S]+)/);
    const fallbackDesc = notes
      .replace(/【オリジナル定義】\n?/, "")
      .replace(/参考[：:][^\n]+\n?/, "")
      .trim();
    return {
      type: "custom",
      refChar:    refM?.[1]?.trim(),
      customDesc: descM?.[1]?.trim() || fallbackDesc,
    };
  }

  if (name || notes) {
    return { type: "unknown", customDesc: [name, notes].filter(Boolean).join("\n") };
  }
  return { type: "leave_it" };
}

/**
 * 受注データから「キャラクター設計支援プロンプト」を生成する。
 * buildCharacterDesignPrompt() と同じ6ブロック出力形式を使う。
 */
export function buildCharacterPromptFromOrder(order: any): string {
  const spec = parseOrderCharSpec(order);
  const cname = order.customer_name || "顧客";

  const REL_DESC: Record<string, string> = {
    "先生": "顧客の「先生」として権威ある立場で指導する。敬語・丁寧語ベースの口調が基本。",
    "先輩": "顧客より年上の「先輩」として接する。経験豊富で引っ張る立場。顧客は後輩として扱う。",
    "後輩": "顧客より年下の「後輩」として接する。顧客のことを「先輩」と呼んで慕う立場。",
  };
  const PERS_DESC: Record<string, string> = {
    "優しい":     "包容力があり失敗しても責めず優しく励ます。穏やかで親しみやすい言葉遣い。",
    "厳しい":     "高い基準を持ち甘えを許さない。歯切れよいストレートな言葉遣い。でも結果にコミット。",
    "クール":     "感情を表に出さず淡々と指導。無口だが核心をついてくる。ミステリアスな魅力。",
    "元気・明るい":"いつでも全力ポジティブ。テンションが高く学習を楽しいものに変える天才。",
    "ツンデレ":   "素直に優しくできないが本当は思いやり深い。照れ隠しで口が悪くなることも。",
  };

  if (spec.type === "leave_it") {
    return buildCharacterDesignPrompt(
      "",
      `英語学習サービス「推しEnglish」で「${cname}」さんと1対1で学習する先生キャラクター。\n` +
      `キャラクターの性別・性格・関係性は運営者に一任。顧客の英語学習を楽しくサポートする。`,
    );
  }

  if (spec.type === "builder") {
    const { gender = "指定なし", rel = "指定なし", pers = "指定なし" } = spec;
    const genderLabel = gender === "男性" ? "男性キャラクター"
      : gender === "女性" ? "女性キャラクター"
      : "性別不問のキャラクター";
    const relCtx  = REL_DESC[rel]  ?? `関係性: ${rel}`;
    const persCtx = PERS_DESC[pers] ?? `性格: ${pers}`;

    // 申し込みフォームで顧客が見た「送信後の流れ」の3ステップ文言
    const voice = getCharVoiceLines(gender, rel, pers);
    const voiceBlock = (voice.step1 || voice.step2 || voice.step3)
      ? `\n\n【申し込み時に顧客が見た「このキャラの口調イメージ」（3ステップ文言）】\n` +
        `顧客はこれらの文言を見てキャラクターに期待を持っている。実際の設計がこの期待と乖離しないよう、口調・呼び方・温度感を揃えること。\n` +
        `📩 アプリでご連絡（第一声）:  ${voice.step1 ?? "（パターン対象外）"}\n` +
        `🎭 キャラクターを完成:     ${voice.step2 ?? "（パターン対象外）"}\n` +
        `🗣️ 学習スタート（毎日チャット）: ${voice.step3 ?? "（パターン対象外）"}`
      : "";

    const description =
      `英語学習サービス「推しEnglish」で「${cname}」さんと1対1で学習する${genderLabel}。\n\n` +
      `【申し込み設定（この設定に忠実なキャラクターを設計すること）】\n` +
      `・性別: ${gender}\n` +
      `・関係性: ${rel} ── ${relCtx}\n` +
      `・性格: ${pers} ── ${persCtx}\n\n` +
      `キャラクターはチャット感覚の日本語テキストで英語学習をサポートする。` +
      `口調・呼び方・一言バリエーション・配色はすべて上記設定を強く反映させること。` +
      voiceBlock;

    return buildCharacterDesignPrompt("", description);
  }

  if (spec.type === "custom" || spec.type === "unknown") {
    const { customDesc = "", refChar } = spec;
    let description =
      `英語学習サービス「推しEnglish」で「${cname}」さんと1対1で学習する先生キャラクター。\n\n` +
      `【顧客が希望するキャラクター設定（この記述を最大限尊重して設計すること）】\n` +
      (customDesc || "（説明なし）");

    if (refChar) {
      description +=
        `\n\n【参考キャラクター（固有名詞は使わず特徴だけを一般名詞で言い換えること）】\n${refChar}`;
    }
    return buildCharacterDesignPrompt("", description);
  }

  return buildCharacterDesignPrompt("", `英語学習サービスの先生キャラクター（顧客: ${cname}）`);
}

export function parseExerciseJsonInput(raw: string): any {
  let text = raw.trim();
  const fenced = text.match(/```(?:json)?\s*([\s\S]*?)```/i);
  if (fenced) text = fenced[1].trim();
  return JSON.parse(text);
}

/** 演習問題データの簡易サマリー（読み込み確認用の表示） */
export function summarizeExerciseData(format: string, data: any): string {
  if (!data || typeof data !== "object") return "";
  if (format === "multiple_choice") {
    const qs = Array.isArray(data.questions) ? data.questions : [];
    const hasScript = !!data.listening_script;
    const hasScoreComments = !!(data.score_comments && (data.score_comments.perfect || data.score_comments.good || data.score_comments.encourage));
    return `選択式：設問 ${qs.length} 問${hasScript ? "／リスニングスクリプトあり" : ""}${data.instructions ? "／案内文あり" : ""}${hasScoreComments ? "／スコア別コメントあり" : "／⚠️スコア別コメント未設定"}`;
  }
  if (format === "written_response") {
    return `記述式：お題${data.prompt ? "あり" : "なし"}／評価観点メモ${data.evaluation_notes ? "あり" : "なし"}／案内文${data.instructions ? "あり" : "なし"}`;
  }
  return "";
}


export function buildSuggestedQuestions(customer: any, character: any): SuggestedQuestion[] {
  const mem = customer?.character_memory ?? {};
  const name = customer?.username ?? "お客さん";
  const month = new Date().getMonth() + 1; // 1〜12

  const seasonal: SuggestedQuestion[] = [];
  if (month === 11 || month === 12) {
    seasonal.push(
      { category: "季節：クリスマス前", icon: "🎄", text: `もうすぐクリスマスだね。${name}さんは、何か予定とかあるの？` },
      { category: "季節：クリスマス前", icon: "🎄", text: `今年はどんなクリスマスを過ごす予定？欲しいものとかある？` },
    );
  }
  if ([1, 2, 5, 6, 10, 11].includes(month)) {
    seasonal.push(
      { category: "季節：試験シーズン前", icon: "📝", text: `そろそろ試験シーズンだね。${name}さんは、何か受ける予定の試験ってあるの？` },
      { category: "季節：試験シーズン前", icon: "📝", text: `試験が近づくと緊張するよね。最近、勉強の調子はどう？無理しすぎてない？` },
    );
  }
  if (month === 3 || month === 4) {
    seasonal.push({ category: "季節：新年度", icon: "🌸", text: `新年度だね！新しい環境とか、何か変わったことってある？` });
  }
  if (month === 7 || month === 8) {
    seasonal.push({ category: "季節：夏休み", icon: "🌻", text: `そろそろ夏休みかな？何か楽しみにしていることってある？` });
  }

  const casual: SuggestedQuestion[] = [
    { category: "何気ない雑談", icon: "💬", text: `最近、なにか面白いことあった？` },
    { category: "何気ない雑談", icon: "💬", text: `今日はどんな一日だった？` },
    { category: "何気ない雑談", icon: "💬", text: `最近ハマってることとか、ある？` },
    { category: "何気ない雑談", icon: "💬", text: `休みの日って、いつもどんなふうに過ごしてるの？` },
  ];

  const birthday: SuggestedQuestion[] = mem?.birthday
    ? [{ category: "誕生日（記録あり）", icon: "🎂", text: `（${mem.birthday}が近ければ）もうすぐ誕生日だね！何か予定はあるの？` }]
    : [
        { category: "誕生日を聞く", icon: "🎂", text: `そういえば、${name}さんの誕生日っていつ？今度ちゃんとお祝いしたいから、よかったら教えて？` },
        { category: "誕生日を聞く", icon: "🎂", text: `誕生日のときって、何か特別なことしたりするの？（さりげなく誕生日を聞き出す）` },
      ];

  const favorites: SuggestedQuestion[] = (mem?.favorites && mem.favorites.length)
    ? [{ category: "好きなもの（記録あり）", icon: "❤️", text: `前に「${mem.favorites[0]}」が好きって言ってたよね。最近はどう？新しい発見とかあった？` }]
    : [
        { category: "好きなものを聞く", icon: "❤️", text: `${name}さんが好きなものとか、ハマってることって何かある？今度の話のネタにしたいから教えてほしいな` },
        { category: "好きなものを聞く", icon: "❤️", text: `休日に絶対やりたいことってある？好きなことを聞かせてほしいな` },
      ];

  return [...seasonal, ...casual, ...birthday, ...favorites];
}


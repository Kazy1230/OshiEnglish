// ── ヘルパー ──────────────────────────────────────────────────────────────────

/** テーブルからキャラ文言を1件取得する */
export function lookupCopy(
  g: GK | null | undefined,
  r: RK | null | undefined,
  p: PK | null | undefined,
  table: CharCopyTable
): string | null {
  if (!g || !r || !p) return null;
  return table[g]?.[r]?.[p] ?? null;
}

/** 申し込みフォームの日本語ラベル → GK キー */
export function toGK(s: string | undefined | null): GK | null {
  const MAP: Record<string, GK> = { "男性": "male", "女性": "female", "どちらでも": "any" };
  return (s && MAP[s]) || null;
}

/** 申し込みフォームの日本語ラベル → RK キー */
export function toRK(s: string | undefined | null): RK | null {
  const MAP: Record<string, RK> = { "先生": "teacher", "先輩": "senpai", "後輩": "kohai" };
  return (s && MAP[s]) || null;
}

/** 申し込みフォームの日本語ラベル → PK キー */
export function toPK(s: string | undefined | null): PK | null {
  const MAP: Record<string, PK> = {
    "優しい": "gentle", "厳しい": "strict", "クール": "cool",
    "元気・明るい": "energetic", "ツンデレ": "tsundere",
  };
  return (s && MAP[s]) || null;
}

/**
 * 日本語ラベルから3ステップの文言をまとめて取得する（管理者画面向け）。
 * @param genderLabel  "男性" | "女性" | "どちらでも"
 * @param relLabel     "先生" | "先輩" | "後輩"
 * @param persLabel    "優しい" | "厳しい" | "クール" | "元気・明るい" | "ツンデレ"
 */
export function getCharVoiceLines(
  genderLabel: string,
  relLabel: string,
  persLabel: string
): { step1: string | null; step2: string | null; step3: string | null } {
  const g = toGK(genderLabel);
  const r = toRK(relLabel);
  const p = toPK(persLabel);
  return {
    step1: lookupCopy(g, r, p, STEP1),
    step2: lookupCopy(g, r, p, STEP2),
    step3: lookupCopy(g, r, p, STEP3),
  };
}

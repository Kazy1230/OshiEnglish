# 記事・演習問題のコンテンツ生成（管理画面の「AIで生成する」機能）のため、
# キャラクターの口調・性格をシステムプロンプトに組み込みつつ、
# 結果をそのままJSONとして管理画面のフォームに反映できるようにするヘルパー。
import json
import re

from app.core.character_voice import render_tone_profile
from app.core.llm import LLMError

_LEVEL_LABELS = {
    "easy": "初級（中学英語レベル。平易な語彙・短い文構造を中心に）",
    "normal": "中級（高校英語〜英検2級・TOEIC600点程度を想定）",
    "hard": "上級（英検準1級〜TOEIC800点以上・大学受験上位レベルを想定）",
}


def _level_block(level: str | None) -> str:
    label = _LEVEL_LABELS.get(level or "normal", _LEVEL_LABELS["normal"])
    return f"■ 想定レベル: {label}"


def _character_block(character) -> str:
    tone_block = render_tone_profile(character.tone_profile if character else None)
    lines = [
        f"■ 名前: {character.name if character else ''}",
        f"■ 説明: {character.description or '' if character else ''}",
    ]
    if tone_block:
        lines.append(tone_block)
    return "\n".join(lines)


_JSON_FENCE_RE = re.compile(r"```(?:json)?\s*(.*?)\s*```", re.DOTALL)


def parse_json_response(text: str) -> dict:
    """LLMの応答からJSON部分を取り出してパースする。コードフェンス付き・無しの両方に対応する。"""
    m = _JSON_FENCE_RE.search(text)
    candidate = m.group(1) if m else text
    try:
        return json.loads(candidate)
    except (json.JSONDecodeError, TypeError) as e:
        raise LLMError(f"AIの応答をJSONとして解析できませんでした: {e}") from e


